void MAIN_vInitSystem(void)
{
  cClockInfo::Update();
  SysTick_Config(cClockInfo::mstClocks.HCLK_Frequency / 100);

  /* STM32L4xx HAL library initialization:
       - Configure the Flash prefetch
       - Systick timer is configured by default as source of time base, but user
         can eventually implement his proper time base source (a general purpose
         timer for example or other time source), keeping in mind that Time base
         duration should be kept 1ms since PPP_TIMEOUT_VALUEs are defined and
         handled in milliseconds basis.
       - Set NVIC Group Priority to 4
       - Low Level Initialization
     */
  HAL_Init();


  MX_GPIO_Init();
  MX_SPI1_Init();
  MX_SPI2_Init();

  /* Configure the SPI2 FIFO Threshold */
  LL_SPI_SetFIFOThreshold(SPI1, LL_SPI_FIFO_TH_01DATA);

  /* Configure SPI1 transfer interrupts */
  /* Enable TXP   Interrupt */
  LL_SPI_EnableIT_TXP(SPI1);
  /* Enable SPI1 Error Interrupt */
  LL_SPI_EnableIT_CRCERR(SPI1);

  /* Configure SPI2 transfer interrupts */
  /* Enable RXP  Interrupt */
  LL_SPI_EnableIT_RXP(SPI2);
  /* Enable SPI2 Error Interrupt */
  LL_SPI_EnableIT_CRCERR(SPI2);

  /* Enable the SPI1 peripheral */
  Activate_SPI1();

  /* Enable the SPI2 peripheral */
  Activate_SPI2();

  /* Wait for User push-button press to start transfer */
  WaitForUserButtonPress();

  /* Start Master transfer */
  LL_SPI_StartMasterTransfer(SPI1);

  /* Wait for the end of the transfer and check received data */
  /* LED blinking FAST during waiting time */
  WaitAndCheckEndOfTransfer();

  CycCall_Start(NULL /*1ms_HP*/,
                NULL /*10ms_HP*/,
                NULL /*100ms_HP*/,
                NULL /*1s_HP*/,

                NULL               /*1ms_LP*/,
                NULL               /*10ms_LP*/,
                NULL               /*100ms_LP*/,
                MAIN_vTick1000msLp /*1s_LP*/);

}




